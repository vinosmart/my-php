        function paginateStudents(page) {
            const startIndex = (page - 1) * studentsPerPage;
            const endIndex = startIndex + studentsPerPage;
            return placedStudentList.slice(startIndex, endIndex);
        }

        function renderPagination() {
            const totalPages = Math.ceil(placedStudentList.length / studentsPerPage);
            let paginationHTML = '';

            for (let i = 1; i <= totalPages; i++) {
                paginationHTML += `
                <li class="${i === currentPage ? 'active' : ''}">
                    <a href="#" onclick="changePage(${i})">${i}</a>
                </li>
            `;
            }

            return `
            <ul>
                <li><a href="#" onclick="changePage(${currentPage - 1})" ${currentPage === 1 ? 'class="disabled"' : ''}><i class="fal fa-angle-left"></i></a></li>
                ${paginationHTML}
                <li><a href="#" onclick="changePage(${currentPage + 1})" ${currentPage === totalPages ? 'class="disabled"' : ''}><i class="fal fa-angle-right"></i></a></li>
            </ul>
        `;
        }

        function changePage(page) {
            const totalPages = Math.ceil(placedStudentList.length / studentsPerPage);
            if (page < 1 || page > totalPages) return;

            currentPage = page;
            const paginatedStudents = paginateStudents(currentPage);
            studentTable.innerHTML = generateTable(paginatedStudents);
            paginationContainer.innerHTML = renderPagination();
        }

